const MESSAGES: Record<Lang, Record<string, string>> = { uz: UZ, ru: RU, en: EN };

function detect(): Lang {
  // Default til — doim "uz", faqat foydalanuvchi til almashtirgichdan o'zi tanlasa o'zgaradi.
  // Brauzer/OS tiliga qarab avtomatik almashtirilmaydi.
  const saved = (typeof localStorage !== "undefined" && localStorage.getItem("lang")) as Lang | null;
  if (saved && saved in MESSAGES) return saved;
  return "uz";
}

let currentLang: Lang = detect();
const listeners = new Set<() => void>();

export function t(key: string, vars?: Record<string, string | number>): string {
  let s = MESSAGES[currentLang][key] ?? MESSAGES.uz[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return s;
}

export function setLang(lang: Lang) {
  currentLang = lang;
  localStorage.setItem("lang", lang);
  listeners.forEach((fn) => fn());
}

export function getLang(): Lang {
  return currentLang;
}

export function useI18n() {
  const [, force] = useState(0);
  useEffect(() => {
    const fn = () => force((n) => n + 1);
    listeners.add(fn);
    return () => { listeners.delete(fn); };
  }, []);
  return { t, lang: currentLang, setLang };
}
